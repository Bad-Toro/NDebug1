public class FTMath {

    public int addEm(int n1, int n2){
        int num1, num2;

        num1 = n1;
        num2 = n2;

        int res = num1 + num2;

        return res;

    }
    public int substractEm(int n1, int n2){
        int num1, num2;

        num1 = n1;
        num2 = n2;

        int res = n1 - n2;

        return res;

    }
    public int multEm(int n1, int n2){
        int num1, num2;

        num1 = n1;
        num2 = n2;

        int res = n1 * n2;

        return res;

    }

}
